pila_dinosaurios = Stack()
for dino in dinosaurios:
    pila_dinosaurios.push(dino)

especies = set()
descubridores = set()
dinos_T = []
dinos_ligeros = []
pila_AQS = Stack()


while pila_dinosaurios.size() > 0:
    dino = pila_dinosaurios.pop()
    especies.add(dino["especie"])
    descubridores.add(dino["descubridor"])

    if dino["nombre"].startswith("T"):
        dinos_T.append(dino["nombre"])

    peso_kg = int(dino["peso"].split()[0])
    if peso_kg < 275:
        dinos_ligeros.append(dino["nombre"])

    if dino["nombre"].startswith(("A", "Q", "S")):
        pila_AQS.push(dino)

print(f"Cantidad de especies: {len(especies)}")
print(f"Cantidad de descubridores distintos: {len(descubridores)}")
print("Dinosaurios que comienzan con T:", dinos_T)
print("Dinosaurios que pesan menos de 275 kg:", dinos_ligeros)

dinos_AQS = [] #pila aparte para los dinosaurios que comienzan con A, Q, S
